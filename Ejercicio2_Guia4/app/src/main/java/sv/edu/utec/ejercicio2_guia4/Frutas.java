package sv.edu.utec.ejercicio2_guia4;

public class Frutas
{
    private String nombre;

    private String info;

    public Frutas(String nombre, String info)
    {
        this.nombre=nombre;
        this.info=info;
    }
    public String getNombre()
    {
        return nombre;
    }
    public String getInfo()
    {
        return info;
    }
}
